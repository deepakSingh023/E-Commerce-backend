const mongoose = require('mongoose');

const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  size: {
    type: String,
    required: true
  },
  category: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  cloudinaryId: {
    type: String,
    required: true
  },
  comments: {
    type: Array,
    default: []
  }
});

module.exports = mongoose.model('Product', productSchema);
